# operator >
print('Apakah benar 5>3 ?', 5>3)
print('Apakah benar 5>5 ?', 5>5)

# operator <
print('Apakah benar 5>3 ?', 5<3)
print('Apakah benar 5>5 ?', 5<5)

# operator >=
print('Apakah benar 2>=3 ?', 2<=3)
print('Apakah benar 10>=20 ?', 10>=20)

# operator <=
print('Apakah benar 5>=3 ?', 5<=3)
print('Apakah benar 5>=5 ?', 5>=5)

# operator ==
print('Apakah benar 5==3 ?', 5==3)
print('Apakah benar 5==5 ?', 5==5)

# operator !=
print('Apakah benar 5!=3 ?', 5!=3)
print('Apakah benar 5!=5 ?', 5!=5)



