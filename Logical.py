# operator and
print('OPERATOR AND')
print('semua statement benar :',
      (5>3) and (9<10))
print('salah satu statement salah :',
      (5<3) and (9<10))
print('--------------')

# operator or
print('OPERATOR OR')
print('semua statement benar :',
      (5>3) or (9<10))
print('salah satu statement salah :',
      (5<3) or (9<10))
print('--------------')

# operator not
print('OPERATOR NOT')
print('statement benar :',not (9<10))
print('statement salah :',not (9>10))
print('--------------')
